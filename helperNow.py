import numpy as np
import math

vectors = [
[[428, 783], [135, 894]],
[[322,  67], [133, 223]],
[[585, 911], [213, 902]],
[[831, 562], [210, 688]],
[[836, 558], [204, 613]],
[[518, 371], [187, 340]],
[[526, 379], [326, 340]],
[[523,  89],  [453, 31]],
[[456,  79],  [231, 67]],
[[412, 728], [128, 872]]
]

newVectors = []
newXs = []
newYs = []

for i in range(10):
    p = vectors[i]
    p1 = p[0]
    p1x = p1[0]
    p1y = p1[1]

    p2 = p[1]
    p2x = p2[0]
    p2y = p2[1]

    newP1 = [0, 0]
    newP2 = [p2x - p1x, p2y - p1y]
    newP = [newP1, newP2]

    newXs.insert(i, newP2[0])
    newYs.insert(i, newP2[1])
    newVectors.insert(i, newP)

medX = np.median(newXs)
medY = np.median(newYs)

print(medX)
print(medY)
